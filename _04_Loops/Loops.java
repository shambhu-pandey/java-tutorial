public class Loops {
  public static void main(String[] args) {
    int i = 1;
    do {
      System.out.println("hello world ");
      i++;
    } while (i <= 10);

    int j = 12;
    do {
      System.out.println("apple");
      j++;
    } while (j <= 11);
  }
}
