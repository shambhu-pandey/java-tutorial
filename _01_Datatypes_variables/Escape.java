package _01_Datatypes_variables;

public class Escape {
  public static void main(String[] args) {
    System.out.println("hello Kg c\'oding");
    System.out.println("hello Kg \"coding\" ");
    System.out.println("hello Kg \\codings\\");
    System.out.println("hello Kg \ncoding");
    System.out.println("hello Kg \tcoding");
    System.out.println("hello Kg\bcoding");
  }
}
